// Escreva um aplicativo em Javascript que mostre todos os números ímpares de 1 até 100
var contador = 0;

while (contador < 100) {
    if (contador % 2 === 1) {
        console.log(contador + " Esse é Ímpar");
    }
    ++contador
}

contador = 0;

while (contador < 100) {
    if (contador % 2 === 0) {
        console.log(contador + " Esse é Par");
    }
    ++contador
}

contador = 0

function imparPar(contador) {
    for (contador = 0; contador < 100; contador++) {
        if (contador % 2 === 1) {
            console.log(contador + " Esse é ímpar");
        } else if (contador % 2 === 0) {
            console.log(contador + " Esse é Par")
        }
    }
}

console.log(imparPar())

